        label = ClassLabel(names=self.config.label_classes)
        if self.config.task_type == TaskType.NER:
            input_type = datasets.Sequence(input)
            label_type = datasets.Sequence(label)
        elif self.config.task_type == TaskType.SLTC:
            input_type = input
            label_type = label
