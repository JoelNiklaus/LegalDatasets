class LextremeConfig(datasets.BuilderConfig):
    """BuilderConfig for LEXTREME."""

    def __init__(
            self,
            input_col,
            label_col,
            url,
            citation,
            hf_hub_name,
            config_name="default",
            label_classes=None,
            task_type=None,
            **kwargs,
    ):
        """BuilderConfig for LEXTREME.
        Args:
          input_col: ``string`, name of the column in the jsonl file corresponding
            to the input
          label_col: `string`, name of the column in the jsonl file corresponding
            to the label
          url: `string`, url for the original project
          citation: `string`, citation for the data set
          hf_hub_name: `string`, huggingface dataset identifier
          config_name: `string`, huggingface dataset config name
          label_classes: `list[string]`, the list of classes if the label is
            categorical. If not provided, then the label will be of type
            `datasets.Value('float32')`.
          task_type: `TaskType`, the task type of the dataset
          **kwargs: keyword arguments forwarded to super.
        """
        super(LextremeConfig, self).__init__(version=datasets.Version("1.0.0", ""), **kwargs)
        self.input_col = input_col
        self.label_col = label_col
        self.url = url
        self.description = textwrap.dedent(self.description)
        self.citation = textwrap.dedent(citation)
        self.hf_hub_name = hf_hub_name
        self.config_name = config_name
        self.label_classes = label_classes
        self.task_type = task_type


